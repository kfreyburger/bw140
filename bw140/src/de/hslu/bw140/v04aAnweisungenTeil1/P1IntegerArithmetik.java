package de.hslu.bw140.v04aAnweisungenTeil1;
public class P1IntegerArithmetik {

	public static void main(String[] args) {
		System.out.println(67 - 16 / 2);
		System.out.println(16 - 4 * 2 + 8);
		System.out.println(16 / 4 * 2 - 8);
		System.out.println((16 - 4) * 2 + 8);
		System.out.println(16 / 4 + 8 % 2);

		int a = 5, b = 6, c;
		c = -(1 + a) * (-b + 2) - b / a + 4 * b % 13 / 2 * 3;
		System.out.println(c);

	}

}
